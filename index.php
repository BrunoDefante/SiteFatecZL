					<div class="box_descricao">

						<p>A Faculdade de Tecnologia da Zona Leste - FATEC-ZL, é uma instituição pública de ensino superior de tecnologia, localizada na cidade de São Paulo, no bairro Cidade Antônio Estêvão de Carvalho (Cid. A.E. Carvalho - distrito de Ponte Rasa). É mantida pelo Centro Estadual de Educação Tecnológica Paula Souza (CEETEPS) que é vinculado a Secretaria do Desenvolvimento do Governo do Estado de São Paulo.

						A FATEC-ZL formava, junto com a ETEC da Zona leste, o Centro Tecnológico da Zona Leste - CTZL, Que foi criado em fevereiro de 2002 e extinto em agosto de 2009, passando, a partir desta data, as duas unidades a existirem separadamente uma da outra.

						O terreno onde foi instalado o CTZL (hoje FATEC e ETEC Zona Leste) estava destinado a receber uma penitenciária, mas protestos dos moradores da região impediram que isso fosse feito, e no terreno que abrigaria uma penitenciária foi instalado a primeira instituição pública de ensino superior da Zona Leste (FATEC Zona Leste).

						Na FATEC Zona Leste até 2009 eram ministrados cursos superiores de tecnologia devidamente reconhecidos, sendo eles Informática para Gestão de Negócios, Logística e Transportes e Produção de Plásticos nos períodos da tarde (a partir das 13:00 horas) e noite (a partir das 19:20 horas).

						Estes cursos, por decisão do Conselho Estadual de Educação foram reformulados em 2009, passando a FATEC-ZL a oferecer, a partir do 1º semestre de 2010 os cursos de Análise e Desenvolvimento e Sistemas - ADS Logística e Polímeros, todos nos mesmos períodos dos cursos anteriores. No 2º semestre de 2010 passou a oferecer o curso Comércio Exterior – COMEX no período da manhã e tarde. Em 2013 foi implantado o curso de Gestão Empresarial.

						A FATEC-ZL está localizada na Av. Águia de Haia, 2983 no mesmo Campus que a ETEC Zona Leste, próximo ao Metrô Artur Alvim, do Terminal A. E. Carvalho e da Estrada do Imperador.</p>

